package day05operators;

public class Increment04 {

	public static void main(String[] args) {
		
		// int data tipinde bir veriable oluşturunç
		// BU veriable ı 1 artırmak için her seferinde 3 yöntemi de kullanın.
		
		int sayi1 = 100;
		
		sayi1 = sayi1 + 1;
		
		System.out.println(sayi1);
		
		sayi1+= 1;
		System.out.println(sayi1);
		
		sayi1++;
		
		System.out.println(sayi1);
			

	}

}
