package day01variables;

public class Variable01 {

	public static void main(String[] args) {
		
	int maas = 3000;
	
	char harf = 'S';
	
	System.out.println(maas);
	
	System.out.println(harf);
	
	int yüzde=40;
	System.out.println(yüzde);
	
	boolean isOld = true; // boolean lar için sadece true veya false için kullanılır.
	
	System.out.println(isOld);
	
	boolean isNew = false;
	
	System.out.println(isNew);
	
	byte derinlik = -123;
	
	System.out.println(derinlik);
	
	short koronaÖlüm = 28000;
	
	System.out.println(koronaÖlüm);
	
	long hucreSayisi = 98653309990984084l; // integer sınırını aşan long variabe lerda en sona 'l' konur
	
	System.out.println(hucreSayisi);
	
	double pi = 3.141497947647467746; // java butun ondalık sayıları otomatık olarak double kabul eder
	System.out.println(pi);
	
	float para = 5.25f;         // eğer bir ondalık kesri double olarak kabul ettirmemek  float yapmak için sonuna 'f' yazılır
	System.out.println(para);
	
	String ogrenciIsmi = "Ali Can";
	System.out.println(ogrenciIsmi);
	
	

	
	
	
	
	
	
	
	
	
	
	
	
	
	
	}
}
